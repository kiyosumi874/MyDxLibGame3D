#include "Timer.h"

//-----------------------------------------------------------------------------
// @brief  コンストラクタ.
//-----------------------------------------------------------------------------
Timer::Timer()
	: startTime()
{
	Start();
}

//-----------------------------------------------------------------------------
// @brief  デストラクタ.
//-----------------------------------------------------------------------------
Timer::~Timer()
{
	// 処理無し.
}

//-----------------------------------------------------------------------------
// @brief  計測開始.
//-----------------------------------------------------------------------------
void Timer::Start()
{
	startTime = system_clock::now();
}

//-----------------------------------------------------------------------------
// @brief  計測開始からの秒数をミリ秒で返す.
//-----------------------------------------------------------------------------
const long long Timer::Elapsed() const
{
	return duration_cast<milliseconds>(system_clock::now() - startTime).count();
}
